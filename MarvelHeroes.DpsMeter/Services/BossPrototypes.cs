    /// <summary>
    /// First high-index value where the dumper's enum encoding diverges from the live network
    /// encoding by exactly +1 (see <see cref="DumperOffByOneThreshold"/> remarks).  Below this
    /// threshold, literal lookup is used as-is.  At/above it, the dual-lookup also probes
    /// <c>idx - 1</c> as a known-bug workaround.
    ///
    /// <para>Empirical evidence (Apr 2026, dps-meter.log session 23:42–04:57):
    /// <list type="table">
    ///   <listheader><term>Live protoIdx</term><description>Resolves to (HeroPrototypes/BossPrototypes idx)</description></listheader>
    ///   <item><term>7269</term><description>"Blade" @ 7269 → MATCH (no shift; user's own avatar)</description></item>
    ///   <item><term>13786</term><description><c>HightownEventSkrullCaptainAmerica</c> @ 13785 → +1 OFF</description></item>
    ///   <item><term>14780</term><description><c>SkrullCommanderNamedSpawnV03</c> @ 14779 → +1 OFF</description></item>
    ///   <item><term>15447</term><description>"Angela" @ 15446 → +1 OFF</description></item>
    ///   <item><term>16067</term><description><c>HightownEventSkrullThor</c> @ 16066 → +1 OFF</description></item>
    ///   <item><term>20193</term><description><c>HightownEventSkrullMsMarvel</c> @ 20192 → +1 OFF</description></item>
    /// </list>
    /// The split is between 9739 ("Hulk", correct) and 13785 (CapAm, off).  Picking 10000 as the
    /// threshold keeps low-index lookups untouched (no risk regression) while applying the
    /// workaround in the entire range where the bug manifests.</para></summary>
    private const uint DumperOffByOneThreshold = 10000u;

    /// <summary>
    /// Strict boss-rank check.  Excludes <see cref="MiniBossIndices"/> by design
    /// (see field doc) — those indices are part of <see cref="Indices"/> for completeness
    /// of the dumper output but are NOT admitted by the boss-only filter at runtime.
    ///
    /// <para><b>Dumper off-by-one workaround.</b>  <c>OpenCalligraphy.AvatarEnumDumper</c>
    /// (<c>CollectClassHierarchy</c>) walks the <i>blueprint inheritance graph</i> when
    /// collecting EntityPrototype-class records, but the live game encodes
    /// <c>NetMessage.prototypeEnumIndex</c> using EmuSource's <c>DataDirectory.InitializeHierarchyCache</c>
    /// scheme — which walks the <i>C# class hierarchy</i> via <c>record.ClassType.BaseType</c>.
    /// Those two trees diverge by exactly one record around index ~10000–13784 (a record whose
    /// C# binding inherits from <c>EntityPrototype</c> but whose blueprint doesn't), so every
    /// dumper index ≥ <see cref="DumperOffByOneThreshold"/> is exactly one less than the live
    /// network value for the same prototype.  Until the dumper is fixed and this file
    /// regenerated, we compensate at lookup time by also probing <c>idx - 1</c>.  False-positive
    /// risk: ~5% per random trash hit at high index (boss density above 10k), with the impact
    /// bounded to "trash damage occasionally counted as boss damage" — far better than the
    /// current "every Hightown patrol boss silently dropped" symptom this fixes.</para></summary>
    public static bool IsBoss(uint prototypeEnumIndex)
        => TryClassifyBoss(prototypeEnumIndex, out _);

    /// <summary>
    /// Same classification as <see cref="IsBoss"/>, but additionally reports whether the match
    /// required the dumper off-by-one fallback (<paramref name="requiredShift"/>=<c>true</c>
    /// means the literal <paramref name="prototypeEnumIndex"/> wasn't in the set but
    /// <c>prototypeEnumIndex - 1</c> was).  Lets callers emit a one-shot diagnostic the first
    /// time the workaround fires for a given index, so the dumper bug stays visible in logs.
    /// </summary>
    public static bool TryClassifyBoss(uint prototypeEnumIndex, out bool requiredShift)
    {
        requiredShift = false;
        if (Indices.Contains(prototypeEnumIndex) && !MiniBossIndices.Contains(prototypeEnumIndex))
            return true;

        if (prototypeEnumIndex >= DumperOffByOneThreshold)
        {
            uint shifted = prototypeEnumIndex - 1u;
            if (Indices.Contains(shifted) && !MiniBossIndices.Contains(shifted))
            {
                requiredShift = true;
                return true;
            }
        }

        return false;
    }

    /// <summary>
    /// True iff the index is one of the dumper's <c>[MiniBoss]</c>-ranked entries.
    /// Useful for diagnostics ("the meter dropped my hit because the target is rank=MiniBoss").
    /// Applies the same dumper off-by-one workaround as <see cref="IsBoss"/> — see that method's
    /// remarks for the rationale.
    /// </summary>
    public static bool IsMiniBoss(uint prototypeEnumIndex)
    {
        if (MiniBossIndices.Contains(prototypeEnumIndex))
            return true;

        if (prototypeEnumIndex >= DumperOffByOneThreshold
            && MiniBossIndices.Contains(prototypeEnumIndex - 1u))
        {
            return true;
        }

        return false;
    }
